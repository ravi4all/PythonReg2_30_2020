'''
range(5)
start -> 0
stop  -> 5 - 1 = 4
step  -> 1
'''
for i in range(5):
    print(i)
print("=============")

'''
range(1,10)
start -> 1
stop  -> 11 - 1 = 10
step  -> 1
'''
for i in range(1,11):
    print(i)
print("=============")
'''
range(2,21,2)
start -> 2
stop  -> 21 - 1 = 20
step  -> 2
'''
for i in range(2,21,2):
    print(i)
print("==============")

for i in range(10,1,-1):
    print(i)



