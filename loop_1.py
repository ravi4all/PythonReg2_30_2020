'''
range(10)
start -> 0
stop  -> 10 - 1 = 9
step = 1
'''
for i in range(10):
    print(i)
print("================")
'''
range(2,21)
start -> 2
stop  -> 21 - 1 = 20
step = 1
'''
for i in range(2,21):
    print(i)

print("================")
'''
range(2,21,2)
start -> 2
stop  -> 21 - 1 = 20
step = 2
'''
for i in range(2,21,2):
    print(i)
